@bot.command()
async def pick(ctx):
    user = ctx.author

    # âœ… Check if the user is in a voice channel
    if not user.voice or not user.voice.channel:
        await ctx.send("âŒ You must be in a voice channel to use this command.")
        return

    vc = user.voice.channel
    members = [m for m in vc.members if not m.bot]

    if not members:
        await ctx.send(f"âŒ No members found in voice channel **{vc.name}**.")
        return

    await ctx.send("How many people do you want to pick? (Reply with a number within 30 seconds)")

    def check(m):
        return m.author == ctx.author and m.channel == ctx.channel and m.content.isdigit()

    try:
        msg = await bot.wait_for("message", timeout=30.0, check=check)
        number = int(msg.content)
        count = min(number, len(members))

        if count <= 0:
            await ctx.send("âŒ Please enter a number greater than 0.")
            return

        selected = random.sample(members, count)
        result = "\n".join(f"- {m.display_name}" for m in selected)

        await ctx.send(f"ğŸ¯ Randomly selected {count} member(s) from **{vc.name}**:\n{result}")

    except asyncio.TimeoutError:
        await ctx.send("â° Timeâ€™s up! Please run the command again.")




